package com.example.lab5_2.WorkList;

import java.io.Serializable;
import java.util.Objects;

public class Work implements Serializable {
    private String name,date,time;

    public Work(String name, String date, String time) {
        this.name = name;
        this.date = date;
        this.time = time;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    @Override
    public String toString() {
        return name + " - "+date + "\t"+time;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Work work = (Work) o;
        return Objects.equals(name, work.name) && Objects.equals(date, work.date) && Objects.equals(time, work.time);
    }

}